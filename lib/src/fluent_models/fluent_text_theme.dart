  final FluentTextStyle? caption1;
  final FluentTextStyle? caption1Strong;
  final FluentTextStyle? caption2;
  final FluentTextStyle? body2;
  final FluentTextStyle? body2Strong;
  final FluentTextStyle? body1;
  final FluentTextStyle? body1Strong;
  final FluentTextStyle? title3;
  final FluentTextStyle? title2;
  final FluentTextStyle? title1;
  final FluentTextStyle? largeTitle;
  final FluentTextStyle? display;

  /// FluentTextTheme's constructor defines fluent text styles.
  FluentTextTheme({
